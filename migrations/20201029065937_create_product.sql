CREATE TABLE `product` (
  `id` BIGINT UNSIGNED PRIMARY KEY NOT NULL AUTO_INCREMENT,
  `name` VARCHAR(64) NOT NULL DEFAULT '' COMMENT '商品官名',
  `alias` VARCHAR(64) NOT NULL DEFAULT '' COMMENT '商品俗称',
  `title` VARCHAR(64) NOT NULL DEFAULT '' COMMENT '商品标题',
  `subtitle` VARCHAR(128) NOT NULL DEFAULT '' COMMENT '商品副标题',
  `brand_id` BIGINT UNSIGNED NOT NULL DEFAULT 0 COMMENT '品牌 brand.id',
  `brand_name` VARCHAR(128) NOT NULL DEFAULT '' COMMENT '品牌名称',
  `spec` VARCHAR(128) NOT NULL DEFAULT '' COMMENT '规格',
  `type` TINYINT NOT NULL DEFAULT 0 COMMENT '类型，0：正装，1：小样',
  `sell_price` DECIMAL(9,2) NOT NULL DEFAULT 0 COMMENT '售价',
  `import_price` DECIMAL(9,2) NOT NULL DEFAULT 0 COMMENT '参考进货价',
  `jd_id` VARCHAR(32) NOT NULL DEFAULT '' COMMENT '参考京东ID',
  `jd_url` VARCHAR(255) NOT NULL DEFAULT '' COMMENT '参考京东链接',
  `img_url` VARCHAR(255) NOT NULL DEFAULT '' COMMENT '图片url',
  `status` TINYINT NOT NULL DEFAULT 0 COMMENT '状态，0：默认，1：已上架，2：已下架',
  `comment` VARCHAR(1024) NOT NULL DEFAULT '' COMMENT '备注',
  `creator` VARCHAR(32) NOT NULL DEFAULT '',
  `modifier` VARCHAR(32) NOT NULL DEFAULT '',
  `created_at` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `updated_at` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  KEY `idx_name` (`name`),
  KEY `idx_title` (`title`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='商品表';
